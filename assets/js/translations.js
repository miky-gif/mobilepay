// Current language (default: French)
let currentLang = localStorage.getItem('moobilpay_lang') || 'fr';

// Initialize translation system
function initTranslation() {
  // Set initial language
  setLanguage(currentLang);
  
  // Add click event to language switcher
  const langSwitcher = document.getElementById('lang-switcher');
  if (langSwitcher) {
    langSwitcher.addEventListener('click', toggleLanguage);
  }
}

// Toggle between languages
function toggleLanguage() {
  currentLang = currentLang === 'fr' ? 'en' : 'fr';
  localStorage.setItem('moobilpay_lang', currentLang);
  setLanguage(currentLang);
  updateLanguageButton();
  showLanguageNotification();
}

// Show notification when language changes
function showLanguageNotification() {
  // Remove existing notification if any
  const existing = document.querySelector('.lang-notification');
  if (existing) {
    existing.remove();
  }
  
  // Create notification
  const notification = document.createElement('div');
  notification.className = 'lang-notification';
  notification.innerHTML = `
    <i class="bi bi-check-circle-fill"></i>
    <span>${currentLang === 'fr' ? 'Langue chang√©e en Fran√ßais' : 'Language changed to English'}</span>
  `;
  
  document.body.appendChild(notification);
  
  // Remove after 3 seconds
  setTimeout(() => {
    notification.remove();
  }, 3000);
}

// Set language and update all translations
function setLanguage(lang) {
  const elements = document.querySelectorAll('[data-i18n]');
  
  elements.forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      // Check if it's an input placeholder
      if (element.hasAttribute('placeholder')) {
        element.setAttribute('placeholder', translations[lang][key]);
      } else {
        element.textContent = translations[lang][key];
      }
    }
  });

  // Update HTML lang attribute
  document.documentElement.lang = lang;
  
  // Add animation class
  document.body.classList.add('lang-switching');
  setTimeout(() => {
    document.body.classList.remove('lang-switching');
  }, 300);
  
  // Update language button
  updateLanguageButton();
}

// Update language button display
function updateLanguageButton() {
  const langBtn = document.getElementById('lang-switcher');
  if (langBtn) {
    const flagSpan = langBtn.querySelector('.lang-flag');
    const textSpan = langBtn.querySelector('.lang-text');
    
    if (currentLang === 'fr') {
      if (flagSpan) flagSpan.textContent = 'üá¨üáß';
      if (textSpan) textSpan.textContent = 'EN';
      langBtn.setAttribute('title', 'Switch to English');
    } else {
      if (flagSpan) flagSpan.textContent = 'üá´üá∑';
      if (textSpan) textSpan.textContent = 'FR';
      langBtn.setAttribute('title', 'Passer en Fran√ßais');
    }
  }
}

